x = int(input("masukan jumlah x: "))
y = int(input("masukan jumlah y: "))
op = input("masukan sistem operasi(+/-/*/%): ")

if op == "+":
    print(x + y)
elif op == "-":
    print(x - y)
elif op == "*":
    print(x * y)
elif op == "%":
    print(x / y)
else:
    print("Sistem operasi tidak ditemukan")
